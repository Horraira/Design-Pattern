// coupling: the degree of interdependence between software modules; the lower the coupling, the better
// cohesion: the degree to which the elements inside a module belong together; the higher the cohesion, the better
// encapsulation: the bundling of data with the methods that operate on that data, or the restricting of direct access to some of an object's components
// inheritance: a mechanism in which one object acquires all the properties and behaviors of a parent object
// polymorphism: the ability of an object to take on many forms
// abstraction: the concept of object-oriented programming that "shows" only essential attributes and "hides" unnecessary information
// class: a blueprint for creating objects
// object: an instance of a class
// method: a function that is associated with an object
// attribute: a variable that is associated with an object and is used to define the properties of an object
// constructor: a special type of method that is used to initialize the object
// package: a namespace that organizes a set of related classes and interfaces
// interface: a reference type in Java that is similar to a class, but its methods are abstract
// abstract class: a class that cannot be instantiated and is designed to be subclassed by other classes
// static: a keyword in Java that is used to create a class method or variable that can be accessed without creating an instance of the class
// final: a keyword in Java that is used to restrict the user from changing the value of a variable, method, or class
// this: a keyword in Java that is used to refer to the current object
// super: a keyword in Java that is used to refer to the immediate parent class object
